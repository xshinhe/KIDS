      ! 1st derivative of fmat with respect to bond coordinates
      dfmat(1,1)=0d0
      dfmat(1,2)=0d0
      dfmat(1,3)=0d0
      do j=2,15
         dfmat(j,1)=dble(j-1)/reoh*fmat(j-1,1) ! (j-1)*((r1-re)/re)^{j-2}
         dfmat(j,2)=dble(j-1)/reoh*fmat(j-1,2) ! (j-1)*((r2-re)/re)^{j-2}
         dfmat(j,3)=dble(j-1)*fmat(j-1,3)      ! (j-1)*(ct-cte)^{j-2}
      enddo

      ! dvc/droh1
      dterm=0d0
      do j=2,245
         term=c5z(j)*(dfmat(idx(j,1),1)*fmat(idx(j,2),2)  &
            &       + dfmat(idx(j,2),1)*fmat(idx(j,1),2)) &
            & *fmat(idx(j,3),3)
         dterm=dterm+term
      end do
      dvcterm(1)=dterm
      dvc_dr(1) = dvcterm(1)*exb + vcterm*dexb_dr(1)

      ! dvc/droh2
      dterm=0d0
      do j=2,245
         term=c5z(j)*(fmat(idx(j,1),1)*dfmat(idx(j,2),2)  &
            &       + fmat(idx(j,2),1)*dfmat(idx(j,1),2)) &
            & *fmat(idx(j,3),3)
         dterm=dterm+term
      end do
      dvcterm(2)=dterm
      dvc_dr(2) = dvcterm(2)*exb + vcterm*dexb_dr(2)

      ! dvc/dcost
      dterm=0d0
      do j=2,245
         term=c5z(j)*(fmat(idx(j,1),1)*fmat(idx(j,2),2)  &
            &       + fmat(idx(j,2),1)*fmat(idx(j,1),2)) &
            & *dfmat(idx(j,3),3)
         dterm=dterm+term
      end do
      dvcterm(3)=dterm
      dvc_dr(3)=dvcterm(3)*exb

      ! dv/dr, r=r1,r2,cost
      dv_dr(1)= dvoh1_droh1 + dvhh_drhh*drhh_dr(1) + dvc_dr(1)
      dv_dr(2)= dvoh2_droh2 + dvhh_drhh*drhh_dr(2) + dvc_dr(2)
      dv_dr(3)= dvhh_drhh*drhh_dr(3) + dvc_dr(3)

      ! finally, calculate dv using the chain rule
      do i = 1,3
         do j = 1,3
            dterm=0d0
            do ir=1,3
               dterm=dterm+dv_dr(ir)*dr_dc(ir,i,j)
            end do
            dv(i,j)=dterm
         end do
      end do

      ! --- calculate ddv ---
      if (flag > 1) then
         ! d(drhh/dr)/dr, r=r1,r2,cost
         ddrhh_drdr(1,1) = (1d0-drhh_dr(1)*drhh_dr(1))/rhh
         ddrhh_drdr(1,2) = (-cost-drhh_dr(1)*drhh_dr(2))/rhh
         ddrhh_drdr(1,3) = (-roh2-drhh_dr(1)*drhh_dr(3))/rhh
         ddrhh_drdr(2,1) = ddrhh_drdr(1,2)
         ddrhh_drdr(2,2) = (1d0-drhh_dr(2)*drhh_dr(2))/rhh
         ddrhh_drdr(2,3) = (-roh1-drhh_dr(2)*drhh_dr(3))/rhh
         ddrhh_drdr(3,1) = ddrhh_drdr(1,3)
         ddrhh_drdr(3,2) = ddrhh_drdr(2,3)
         ddrhh_drdr(3,3) = (-drhh_dr(3)*drhh_dr(3))/rhh

         ! 2nd derivative of Va(r1)
         ddvoh1_droh1droh1=2d0*deoh*alphaoh*alphaoh*exa(1)*(2d0*exa(1)-1d0)

         ! 2nd derivative of Va(r2)
         ddvoh2_droh2droh2=2d0*deoh*alphaoh*alphaoh*exa(2)*(2d0*exa(2)-1d0)

         ! 2nd derivatives of Vb(rhh)
         ddvhh_drhhdrhh=-phh2*dvhh_drhh
         do i = 1,3
            do j = 1,3
               ddvhh_drdr(i,j) = drhh_dr(i)*ddvhh_drhhdrhh*drhh_dr(j) &
                  + dvhh_drhh*ddrhh_drdr(i,j)
            end do
         end do

         ! 2nd derivative of exb with respect to bond coordinates
         ! exb = exp(-b1*((roh1-reoh)**2+(roh2-reoh)**2))
         ddexb_drdr(1,1) = -2d0*b1*(exb + (roh1-reoh)*dexb_dr(1))
         ddexb_drdr(1,2) = -2d0*b1*(roh2-reoh)*dexb_dr(1)
         ddexb_drdr(2,1) = ddexb_drdr(1,2)
         ddexb_drdr(2,2) = -2d0*b1*(exb + (roh2-reoh)*dexb_dr(2))

         ! d(d(vcterm)/dr)/dr
         ddfmat(1,:)=0d0
         ddfmat(2,:)=0d0
         do j = 3,15
            ddfmat(j,1)=dble(j-1)/reoh*dfmat(j-1,1) !(j-1)/re*(j-2)*((r1-re)/re)^{j-3}
            ddfmat(j,2)=dble(j-1)/reoh*dfmat(j-1,2) !(j-1)/re*(j-2)*((r2-re)/re)^{j-3}
            ddfmat(j,3)=dble(j-1)*dfmat(j-1,3)      !(j-1)   *(j-2)*(ct-cte)^{j-3}
         end do

         ! --- d(dvc/dr)/dr, r=r1,r2,cost ---
         ! d(dvc/droh1)/droh1
         ddterm=0d0
         do j=2,245
            term=c5z(j)*(ddfmat(idx(j,1),1)*fmat(idx(j,2),2)  &
               &       + ddfmat(idx(j,2),1)*fmat(idx(j,1),2)) &
               & *fmat(idx(j,3),3)
            ddterm=ddterm+term
         end do
         ddvc_drdr(1,1)=ddterm*exb+2d0*dvcterm(1)*dexb_dr(1)+vcterm*ddexb_drdr(1,1)

         ! d(dvc/droh1)/droh2
         ddterm=0d0
         do j=2,245
            term=c5z(j)*(dfmat(idx(j,1),1)*dfmat(idx(j,2),2)  &
               &       + dfmat(idx(j,2),1)*dfmat(idx(j,1),2)) &
               & *fmat(idx(j,3),3)
            ddterm=ddterm+term
         end do
         ddvc_drdr(1,2)=ddterm*exb+dvcterm(1)*dexb_dr(2) &
            +dvcterm(2)*dexb_dr(1)+vcterm*ddexb_drdr(1,2)

         ! d(dvc/droh1)/dcost
         ddterm=0d0
         do j=2,245
            term=c5z(j)*(dfmat(idx(j,1),1)*fmat(idx(j,2),2)  &
               &       + dfmat(idx(j,2),1)*fmat(idx(j,1),2)) &
               & *dfmat(idx(j,3),3)
            ddterm=ddterm+term
         end do
         ddvc_drdr(1,3)=ddterm*exb + dvcterm(3)*dexb_dr(1)

         ! d(dvc/droh2)/droh1
         ddvc_drdr(2,1)=ddvc_drdr(1,2)

         ! d(dvc/droh2)/droh2
         ddterm=0d0
         do j=2,245
            term=c5z(j)*(fmat(idx(j,1),1)*ddfmat(idx(j,2),2)  &
               &       + fmat(idx(j,2),1)*ddfmat(idx(j,1),2)) &
               & *fmat(idx(j,3),3)
            ddterm=ddterm+term
         end do
         ddvc_drdr(2,2) = ddterm*exb +2d0*dvcterm(2)*dexb_dr(2)+vcterm*ddexb_drdr(2,2)

         ! d(dvc/droh2)/dcost
         ddterm=0d0
         do j=2,245
            term=c5z(j)*(fmat(idx(j,1),1)*dfmat(idx(j,2),2)  &
               &       + fmat(idx(j,2),1)*dfmat(idx(j,1),2)) &
               & *dfmat(idx(j,3),3)
            ddterm=ddterm+term
         end do
         ddvc_drdr(2,3)=ddterm*exb + dvcterm(3)*dexb_dr(2)

         ! d(dvc/dcost)/droh1
         ddvc_drdr(3,1)=ddvc_drdr(1,3)

         ! d(dvc/dcost)/droh2
         ddvc_drdr(3,2)=ddvc_drdr(2,3)

         ! d(dvc/dcost)/dcost
         ddterm=0d0
         do j=2,245
            term=c5z(j)*(fmat(idx(j,1),1)*fmat(idx(j,2),2)  &
               &       + fmat(idx(j,2),1)*fmat(idx(j,1),2)) &
               & *ddfmat(idx(j,3),3)
            ddterm=ddterm+term
         end do
         ddvc_drdr(3,3)=ddterm*exb

         ! d(dv/dr)/dr (r=r1,r2,cost)
         ddv_drdr(1,1) = ddvoh1_droh1droh1 + ddvhh_drdr(1,1) + ddvc_drdr(1,1)
         ddv_drdr(1,2) = ddvhh_drdr(1,2) + ddvc_drdr(1,2)
         ddv_drdr(1,3) = ddvhh_drdr(1,3) + ddvc_drdr(1,3)

         ddv_drdr(2,1) = ddv_drdr(1,2)
         ddv_drdr(2,2) = ddvoh2_droh2droh2 + ddvhh_drdr(2,2) + ddvc_drdr(2,2)
         ddv_drdr(2,3) = ddvhh_drdr(2,3) + ddvc_drdr(2,3)

         ddv_drdr(3,1) = ddv_drdr(1,3)
         ddv_drdr(3,2) = ddv_drdr(2,3)
         ddv_drdr(3,3) = ddvhh_drdr(3,3) + ddvc_drdr(3,3)

         ! finally, calculate the 2nd derivatives d(dv/dc_{i,j})/dc_{k,l} with the chain rule
         do i=1,3 ! atom H1,O,H2
            do j=1,3 ! x,y,z
               do k=1,3 ! atom H1,O,H2
                  do l=1,3 ! x,y,z
                     ddterm=0d0
                     do ir=1,3 ! dv/dr_ir
                        term=0d0
                        do jr=1,3  ! d(dv/dr_ir)/dr_jr
                           term=term + ddv_drdr(ir,jr)*dr_dc(jr,k,l)
                        end do
                        ddterm=ddterm+ term*dr_dc(ir,i,j) + dv_dr(ir)*ddr_dcdc(ir,i,j,k,l)
                     end do
                     ddv(i,j,k,l)=ddterm
                  end do
               end do
            end do
         end do ! end of calculate ddv loop
      end if ! end of if calculate ddv
   end if  ! end of if calculate dv
   return
end subroutine h2opot1

!-----------------------------------------------------------------------------
!> convert cartesian coordinates to geometry,
!> and calculate derivatives if need (set flag)
!!
!> @param [out] roh1, bond length of O--H1
!> @param [out] roh2, bond length of O--H2
!> @param [out] cost, cosine of the bond angle H1-O--H2
!> @param [out] rhh, bond length of H1-H2
!> @param [out] [optional], dr_dc, 1st derivatives of r=roh1,roh2,cost,
!>              with respect to c=x,y,z of H1,O,H2
!> @param [out] [optional], ddr_dcdc, 2nd derivatives of r=roh1,roh2,cost,
!>              with respect to c=x,y,z of H1,O,H2
!> @param [in]  cart(3,3): Cartesian coordinates for each atom in a.u.
!> @param [in]  flag: controls calculation,
!               0 -- v; 1 -- v,dv; 2 -- v,dv,ddv
!-----------------------------------------------------------------------------
subroutine convert_cart_to_geom(roh1, roh2, cost, rhh, dr_dc, ddr_dcdc, cart, flag)
   implicit none
   !> bond coordinates: bond lengths and cosine of bond angle H1-O-H1
   real(8) :: roh1, roh2, rhh, cost

   !> partial derivatives of r1, r2, cos(theta) : dr/dc
   real(8) :: dr_dc(3,3,3), ddr_dcdc(3,3,3,3,3)

   !> cartesian coordinates of H1, O, H2
   real(8), dimension(3,3) :: cart

   !> flag that controls calculation of r, dr_dc, ddr_dcdc
   integer :: flag

   ! difference between cartesian coordinates of atoms
   real(8) :: xoh1, xoh2, xhh, yoh1, yoh2, yhh, zoh1, zoh2, zhh

   ! temporary values
   real(8) :: r1sq, r2sq, r3sq, r1r2

   ! index
   integer :: i,j,k

   ! convert cart to geometry
   ! calculate the valence coordinates from Cartesian coordinates
   ! H1-O bond length, in a.u.
   xoh1 = cart(1,1)-cart(2,1)
   yoh1 = cart(1,2)-cart(2,2)
   zoh1 = cart(1,3)-cart(2,3)
   r1sq = xoh1**2 + yoh1**2 + zoh1**2
   roh1 = sqrt(r1sq)

   ! H2-O bond length, in a.u.
   xoh2 = cart(3,1)-cart(2,1)
   yoh2 = cart(3,2)-cart(2,2)
   zoh2 = cart(3,3)-cart(2,3)
   r2sq = xoh2**2 + yoh2**2 + zoh2**2
   roh2 = sqrt(r2sq)

   ! H2-H1 bond length, in a.u.
   xhh  = cart(1,1)-cart(3,1)
   yhh  = cart(1,2)-cart(3,2)
   zhh  = cart(1,3)-cart(3,3)
   r3sq = xhh**2 + yhh**2 + zhh**2
   rhh  = sqrt(r3sq)

   ! cosine of theta, the H1-O-H2 bond angle
   r1r2 = roh1*roh2
   cost = 0.5d0*(r1sq + r2sq - r3sq)/r1r2
   if (cost < -1d0) cost=-1d0
   if (cost > 1d0) cost=1d0

   ! --- calculate derivatives ---
   ! calculate 1st derivative
   if (flag > 0) then
      ! droh1_duh1 (u=x,y,z)
      dr_dc(1,1,1) = xoh1/roh1    ! droh1_dxh1
      dr_dc(1,1,2) = yoh1/roh1    ! droh1_dyh1
      dr_dc(1,1,3) = zoh1/roh1    ! droh1_dzh1

      ! droh1_duh2 (u=x,y,z)
      dr_dc(1,3,:) = 0d0

      ! droh1_duo (u=x,y,z)
      dr_dc(1,2,:) = -dr_dc(1,1,:)   ! droh1_dxo

      ! droh2_duh1 (u=x,y,z)
      dr_dc(2,1,:) = 0d0

      ! droh2_duh2 (u=x,y,z)
      dr_dc(2,3,1) = xoh2/roh2    ! droh2_dxh2
      dr_dc(2,3,2) = yoh2/roh2    ! droh2_dyh2
      dr_dc(2,3,3) = zoh2/roh2    ! droh2_dzh2

      ! droh2_duo (u=x,y,z)
      dr_dc(2,2,:) = -dr_dc(2,3,:)   ! droh2_dxo

      ! dcost_duh1 (u=x,y,z)
      dr_dc(3,1,:) = (dr_dc(2,3,:)-cost*dr_dc(1,1,:))/roh1

      ! dcost_duh2 (u=x,y,z)
      dr_dc(3,3,:) = (dr_dc(1,1,:)-cost*dr_dc(2,3,:))/roh2

      ! dcost_duo (u=x,y,z)
      dr_dc(3,2,:) = -dr_dc(3,1,:)-dr_dc(3,3,:)

      ! calculate 2nd derivatives: d(dr/dc)/dc
      if (flag > 1) then
         ! --- d(dr1/dc)/dc ---
         ! d(dr1/dxH1)/dcH1
         do i=1,3
            ddr_dcdc(1,1,i,1,i) = (1d0-dr_dc(1,1,i)*dr_dc(1,1,i))/roh1
         enddo
         ddr_dcdc(1,1,1,1,2) = -dr_dc(1,1,1)*dr_dc(1,1,2)/roh1
         ddr_dcdc(1,1,1,1,3) = -dr_dc(1,1,1)*dr_dc(1,1,3)/roh1

         ! d(dr1/dyH1)/dcH1
         ddr_dcdc(1,1,2,1,1) = ddr_dcdc(1,1,1,1,2)
         ddr_dcdc(1,1,2,1,3) = -dr_dc(1,1,2)*dr_dc(1,1,3)/roh1

         ! d(dr1/dzH1)/dcH1
         ddr_dcdc(1,1,3,1,1) = ddr_dcdc(1,1,1,1,3)
         ddr_dcdc(1,1,3,1,2) = ddr_dcdc(1,1,2,1,3)

         ! d(dr1/dcH1)/dcO   (c=x,y,z)
         do i = 1,3
            do j = 1,3
               ddr_dcdc(1,1,i,2,j) = -ddr_dcdc(1,1,i,1,j)
            end do
         enddo

         ! d(dr1/dcH1)/dcH2  (c=x,y,z)
         ddr_dcdc(1,1,:,3,:) = 0d0

         ! d(dr1/dxO)/dc
         do i=1,3
            do j=1,3
               do k=1,3
                  ddr_dcdc(1,2,i,j,k) = -ddr_dcdc(1,1,i,j,k)
               enddo
            enddo
         enddo

         ! d(dr1/dcH2)/dc
         ddr_dcdc(1,3,:,:,:) = 0d0

         ! --- d(dr2/dc)/dc ---
         ! d(dr2/dcH1)/dc
         ddr_dcdc(2,1,:,:,:) = 0d0

         ! d(dr2/dxH2)/dcH2
         do i=1,3
            ddr_dcdc(2,3,i,3,i) = (1d0-dr_dc(2,3,i)*dr_dc(2,3,i))/roh2
         enddo
         ddr_dcdc(2,3,1,3,2) = -dr_dc(2,3,1)*dr_dc(2,3,2)/roh2
         ddr_dcdc(2,3,1,3,3) = -dr_dc(2,3,1)*dr_dc(2,3,3)/roh2

         ! d(dr2/dyH2)/dcH2
         ddr_dcdc(2,3,2,3,1) = ddr_dcdc(2,3,1,3,2)
         ddr_dcdc(2,3,2,3,3) = -dr_dc(2,3,2)*dr_dc(2,3,3)/roh2

         ! d(dr2/dzH2)/dcH2
         ddr_dcdc(2,3,3,3,1) = ddr_dcdc(2,3,1,3,3)
         ddr_dcdc(2,3,3,3,2) = ddr_dcdc(2,3,2,3,3)

         ! d(dr2/dcH2)/dcH1
         ddr_dcdc(2,3,:,1,:) = 0d0

         ! d(dr2/dcH2)/dcO
         do i=1,3
            do j=1,3
               ddr_dcdc(2,3,i,2,j) = -ddr_dcdc(2,3,i,3,j)
            enddo
         enddo

         ! d(dr2/dcO)/dc
         do i=1,3
            do j=1,3
               do k=1,3
                  ddr_dcdc(2,2,i,j,k) = -ddr_dcdc(2,3,i,j,k)
               enddo
            enddo
         enddo

         ! --- d(dcost/dc)/dc ---
         ! --- d(dcost/dcH1)/dc ---
         ! d(dcost/dxH1)/dcH1
         do i=1,3
            ddr_dcdc(3,1,i,1,i) = -(cost*ddr_dcdc(1,1,i,1,i) &
               + 2d0*dr_dc(1,1,i)*dr_dc(3,1,i))/roh1
         enddo

         ddr_dcdc(3,1,1,1,2) = -(cost*ddr_dcdc(1,1,1,1,2) &
            + dr_dc(1,1,2)*dr_dc(3,1,1) + dr_dc(3,1,2)*dr_dc(1,1,1))/roh1

         ddr_dcdc(3,1,1,1,3) = -(cost*ddr_dcdc(1,1,1,1,3) &
            + dr_dc(1,1,3)*dr_dc(3,1,1) + dr_dc(3,1,3)*dr_dc(1,1,1))/roh1

         ! d(dcost/dyH1)/dcH1
         ddr_dcdc(3,1,2,1,1)=ddr_dcdc(3,1,1,1,2)
         ddr_dcdc(3,1,2,1,3) = -(cost*ddr_dcdc(1,1,2,1,3) &
            + dr_dc(1,1,3)*dr_dc(3,1,2) + dr_dc(3,1,3)*dr_dc(1,1,2))/roh1

         ! d(dcost/dzH1)/dcH1
         ddr_dcdc(3,1,3,1,1)=ddr_dcdc(3,1,1,1,3)
         ddr_dcdc(3,1,3,1,2)=ddr_dcdc(3,1,2,1,3)

         ! d(dcost/dcH1)/dcO
         do i=1,3
            do j=1,3
               ddr_dcdc(3,1,i,2,j) = -(cost*ddr_dcdc(1,1,i,2,j) &
                  + dr_dc(1,2,j)*dr_dc(3,1,i) + dr_dc(3,2,j)*dr_dc(1,1,i) &
                  - ddr_dcdc(2,3,i,2,j))/roh1
            enddo
         enddo

         ! d(dcost/dcH1)/dcH2
         do i=1,3
            do j=1,3
               ddr_dcdc(3,1,i,3,j)=(ddr_dcdc(2,3,i,3,j)-dr_dc(3,3,j)*dr_dc(1,1,i))/roh1
            enddo
         enddo

         ! --- d(dcost/dcH2)/dc ---
         ! d(dcost/dcH2)/dcH1
         do i=1,3
            do j=1,3
               ddr_dcdc(3,3,i,1,j) = ddr_dcdc(3,1,j,3,i)
            enddo
         enddo

         ! d(dcost/dcH2)/dcO
         do i=1,3
            do j=1,3
               ddr_dcdc(3,3,i,2,j) = -(cost*ddr_dcdc(2,3,i,2,j) &
                  + dr_dc(2,2,j)*dr_dc(3,3,i) + dr_dc(3,2,j)*dr_dc(2,3,i) &
                  - ddr_dcdc(1,1,i,2,j))/roh2
            enddo
         enddo

         ! d(dcost/dcH2)/dcH2
         ! d(dcost/dcH2)/dcH2 (c=c, c=x,y,z)
         do i=1,3
            ddr_dcdc(3,3,i,3,i) = -(cost*ddr_dcdc(2,3,i,3,i) &
               + 2d0*dr_dc(2,3,i)*dr_dc(3,3,i))/roh2
         enddo

         ! d(dcost/dxH2)/dcH2 (c=y,z)
         ddr_dcdc(3,3,1,3,2) = -(cost*ddr_dcdc(2,3,1,3,2) &
            + dr_dc(2,3,2)*dr_dc(3,3,1) + dr_dc(3,3,2)*dr_dc(2,3,1))/roh2

         ddr_dcdc(3,3,1,3,3) = -(cost*ddr_dcdc(2,3,1,3,3) &
            + dr_dc(2,3,3)*dr_dc(3,3,1) + dr_dc(3,3,3)*dr_dc(2,3,1))/roh2

         ! d(dcost/dyH2)/dcH2
         ddr_dcdc(3,3,2,3,1)=ddr_dcdc(3,3,1,3,2)
         ddr_dcdc(3,3,2,3,3) = -(cost*ddr_dcdc(2,3,2,3,3) &
            + dr_dc(2,3,3)*dr_dc(3,3,2) + dr_dc(3,3,3)*dr_dc(2,3,2))/roh2

         ! d(dcost/dzH2)/dcH2
         ddr_dcdc(3,3,3,3,1)=ddr_dcdc(3,3,1,3,3)
         ddr_dcdc(3,3,3,3,2)=ddr_dcdc(3,3,2,3,3)

         ! --- d(dcost/dcO)/dc ---
         do i=1,3
            do j=1,3
               ddr_dcdc(3,2,i,1,j)=ddr_dcdc(3,1,j,2,i)
               ddr_dcdc(3,2,i,2,j)=-ddr_dcdc(3,1,i,2,j)-ddr_dcdc(3,3,i,2,j)
               ddr_dcdc(3,2,i,3,j)=ddr_dcdc(3,3,j,2,i)
            enddo
         enddo
      end if
   end if
   return
end subroutine convert_cart_to_geom

!----------------------------------------------------------------------
!> convert geometry (r1, r2, theta) to cartesian coordinates
!> set O at (0,0,0), H1 on x-axis
!> @param [out] cart(9), in au, 1-3: H1, 4-6: O, 7-9: H2
!> @param [in] geom(3), r1, r2, theta, r1,r2 in au, theta in rad
!----------------------------------------------------------------------
subroutine convert_geom_to_cart(cart,geom)
   implicit none
   real(8),dimension(3) :: geom
   real(8),dimension(9) :: cart
   real(8),parameter :: PI = 3.14159265358979323846d0

   ! check input
   if (geom(1) <= 0d0) then
      write(*,*)'ERROR: input r1 is incorrect!'
      stop
   end if
   if (geom(2) <= 0d0) then
      write(*,*)'ERROR: input r2 is incorrect!'
      stop
   end if
   if (geom(3) < 0d0 .or. geom(3) > PI) then
      write(*,*)'ERROR: input theta is incorrect!'
      stop
   end if
   ! set O at (0,0,0)
   ! the H2O molecule on x-y plane, OH1 on x-axis
   cart(1)=geom(1)
   cart(2:6) = 0d0
   cart(7)=geom(2)*cos(geom(3))
   cart(8)=geom(2)*sin(geom(3))
   cart(9) = 0d0
   return
end subroutine convert_geom_to_cart
