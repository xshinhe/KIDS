!--------------------------------------------------------
!> calculate energy and derivatives for H2O PES
!> coordinate form slightly different to h2opot1
!> 1-3: x,y,z of H1
!> 4-6: x,y,z of O
!> 7-9: x,y,z of H2
!>
!> @param [out] v, potential energy
!> @param [out] dv, gradients of potential energy
!> @param [out] ddv, second derivatives of v
!> @param [in] cart, cartesian coordinates
!>
!> @todo change h2opot1 with this coordinate form in the future
!-------------------------------------------------------------------
subroutine h2opot(v,dv,ddv,cart,flag) bind(c, name="h2opot_ccall")
   implicit none
   real(8), intent(out) :: v
   real(8), dimension(9), intent(out) :: dv
   real(8), dimension(9,9), intent(out) :: ddv
   real(8), dimension(9), intent(in) :: cart
   integer, intent(in) :: flag
!f2py intent(out) v
!f2py intent(out) dv
!f2py intent(out) ddv
!f2py intent(in) cart
!f2py intent(in) flag

   ! variables for calling h2opot
   real(8), dimension(3,3) :: dv1
   real(8), dimension(3,3,3,3) :: ddv1
   real(8), dimension(3,3) :: cart1

   integer :: i, j, k, l

   ! convert cartesian coordinates form, 1-D array to 2-D array
   do i = 1, 3
      do j = 1,3
         cart1(i,j) = cart((i-1)*3+j)
      end do
   end do

   ! call h2opot1 with 2-D array for dv, 4-D array for ddv
   call h2opot1(v,dv1,ddv1,cart1,flag)

   ! convert dv from 2-D array to 1-D array
   do i = 1, 3
      do j = 1,3
         dv((i-1)*3+j) = dv1(i,j)
      end do
   end do

   ! convert ddv from 4-D array to 2-D array
   do i = 1,3
      do j = 1,3
         do k = 1,3
            do l = 1,3
               ddv((i-1)*3+j, (k-1)*3+l) = ddv1(i,j,k,l)
            end do
         end do
      end do
   end do

   return
end subroutine h2opot


